/// Defines the visual style variants for [MinimalTabs].
enum TabVariant {
  /// Tabs with underline indicator
  underline,

  /// Pill-shaped tabs
  pill,

  /// Connected segmented tabs
  segmented,
}

/// Defines the size variants for [MinimalTabs].
enum TabSize {
  /// Small tabs
  sm,

  /// Medium tabs (default)
  md,

  /// Large tabs
  lg,
}
